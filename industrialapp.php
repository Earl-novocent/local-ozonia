							<h3>Ozone: The Green Solution for Pulp Bleaching</h3>
						    <p>Ozone is considered, from an ecological point of view, to be the best candidate for the bleaching of both hardwood and softwood pulp, in replacement for chlorine, in a combination-competition approach with other bleaching chemicals, such as Chlorine Dioxide, Hydrogen Peroxide and per-Acetic Acid.</p>
						    <p>Ozone is used, either on High-Consistency pulps (HC), containing 30 to 40% dry solids, or on Medium-Consistency pulp (MC), with 10 to 12% dry solids contents. In each case the process of mixing the gas with the pulp is the key factor.</p>
						    <h2>HC Bleaching</h23>
						    <p>The pulp is preliminary acidified and pressed until a consistency around 40%. A "drum" mixer is then used to optimize the contact of the gas with the pulp. In these cases, mixers operating at 2 bar are used.</p>
						    <h2>MC Bleaching</h2>
						    <p>The acidified pulp is directly treated in a mixer. In this case, mixing takes place at 12-13 bar. This assumes controlled compression without destroying the ozone.</p>
						    <p>In both cases, the oxygen off-gases can be reused in the client's process, either for oxygen delignification, oxidation of white liquor or for the waste water treatment stage, decreasing consequently the ozone variable cost.</p>
